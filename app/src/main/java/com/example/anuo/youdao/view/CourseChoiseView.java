package com.example.anuo.youdao.view;

public class CourseChoiseView {
    private int imgId;
    private String name;

    public CourseChoiseView(int imgId, String name) {
        this.imgId = imgId;
        this.name = name;
    }

    public int getImgId() {
        return imgId;
    }

    public String getName() {
        return name;
    }
}
